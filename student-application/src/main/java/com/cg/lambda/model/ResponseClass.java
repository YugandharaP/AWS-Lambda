package com.cg.lambda.model;

public class ResponseClass {
	String msg;
	String status;

	public ResponseClass() {
		
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}
}
